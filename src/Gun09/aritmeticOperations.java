package Gun09;

public class aritmeticOperations {
    public static void main(String[] args) {
        int a=10;
        int b=5;

        System.out.println("Çarpma işlemi");
        System.out.println("a*b = " + (a*b));

        System.out.println("Bölme işlemi");
        System.out.println("a/b = " + (a/b));

        System.out.println("Modül işlemi"); // Modül -> bölümünden kalan anlamına gelir!
        System.out.println("a%b = " + (a % b));
    }
}
