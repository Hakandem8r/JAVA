package cumartesiMentoring03;

public class doWhile02 {
    public static void main(String[] args) {
        int count = 11;
        while (count < 11) {
            System.out.println("count is: "+ count);
            count++;
        }
        int doCount = 11;
        do{
            System.out.println("doCount is: "+ doCount);
            doCount++;
        }while(doCount < 11);
    }
}
