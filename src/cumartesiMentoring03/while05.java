package cumartesiMentoring03;

public class while05 {
    public static void main(String[] args) {
        int x = 3;
        int i = 0;
        while (i < 3) {
            x+= 1; // x = x + 1
            i += 1;
        }
        System.out.println("x= "+ x);
        System.out.println("i= "+ i);
    }
}
