package Gun16ForLoop;

public class forLoop02 {
    public static void main(String[] args) {
        //0 d0n 10 a kadar sayıları ekrana yazdırın
        // 10 dan 0 a kadar sayıları ekrana yazdırın

        for (int i=0; i<=10; i++){
            System.out.println("i_i= "+i);
        }
        System.out.println(".........................");
        for(int i=10; i>=0; i--){
            System.out.println("g_i = " + i);
        }
    }
}
